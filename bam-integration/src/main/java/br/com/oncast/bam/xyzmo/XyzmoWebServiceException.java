package br.com.oncast.bam.xyzmo;

public class XyzmoWebServiceException extends Exception {
	private static final long serialVersionUID = 1L;

	public XyzmoWebServiceException() {
		super();
	}

	public XyzmoWebServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public XyzmoWebServiceException(String arg0) {
		super(arg0);
	}

	public XyzmoWebServiceException(Throwable arg0) {
		super(arg0);
	}
	
	

}
